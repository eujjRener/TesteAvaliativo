def descobrir_interruptores():
    
    import time
    
    print("Passo 1: Ligue o primeiro interruptor e deixe-o ligado por alguns minutos.")
    print("Passo 2: Desligue o primeiro interruptor e ligue o segundo interruptor.")
    print("Passo 3: Vá até a sala das lâmpadas e verifique o estado das lâmpadas:")
    
   
    time.sleep(2)  
    
    print("- A lâmpada que está acesa corresponde ao segundo interruptor.")
    print("- A lâmpada que está apagada, mas quente, corresponde ao primeiro interruptor.")
    print("- A lâmpada apagada e fria corresponde ao terceiro interruptor.")

descobrir_interruptores()